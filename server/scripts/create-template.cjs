const { Document, Paragraph, TextRun, Packer } = require('docx');
const fs = require('fs');
const path = require('path');

async function createTemplate() {
  const doc = new Document({
    sections: [{
      properties: {},
      children: [
        new Paragraph({
          children: [
            new TextRun({ text: 'TRADEMARK APPLICATION FORM', bold: true, size: 32 })
          ],
          spacing: { after: 400 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Applicant Name: ', bold: true }),
            new TextRun({ text: '{case.client_name}' })
          ],
          spacing: { after: 200 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Mark Name: ', bold: true }),
            new TextRun({ text: '{case.mark_name}' })
          ],
          spacing: { after: 200 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Jurisdiction: ', bold: true }),
            new TextRun({ text: '{case.jurisdiction}' })
          ],
          spacing: { after: 200 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Address: ', bold: true }),
            new TextRun({ text: '{case.address_street}' })
          ],
          spacing: { after: 200 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Nationality: ', bold: true }),
            new TextRun({ text: '{case.nationality}' })
          ],
          spacing: { after: 200 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Date Filed: ', bold: true }),
            new TextRun({ text: '{date}' })
          ],
          spacing: { after: 200 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Generated By: ', bold: true }),
            new TextRun({ text: '{generated_by}' })
          ],
          spacing: { after: 400 }
        }),
        new Paragraph({
          children: [
            new TextRun({ text: '--- End of Form ---', italics: true })
          ]
        })
      ]
    }]
  });

  const buffer = await Packer.toBuffer(doc);
  const templateDir = path.join(process.cwd(), 'templates');
  if (!fs.existsSync(templateDir)) {
    fs.mkdirSync(templateDir, { recursive: true });
  }
  fs.writeFileSync(path.join(templateDir, 'form01.docx'), buffer);
  console.log('Template created successfully at templates/form01.docx');
}

createTemplate().catch(console.error);
